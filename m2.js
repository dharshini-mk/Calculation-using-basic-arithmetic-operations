exports.add = function(a, b)
{
    return a + b;
}
exports.sub = function(a, b) 
{
    return a - b;
}
exports.product = function(a, b)
{
    return a * b;
}
exports.divide = function(a, b) 
{
    return a / b;
}
exports.modulo = function(a, b) 
{
    return a % b;
}
exports.power = function(a, b) 
{
    return Math.pow(a, b);
}